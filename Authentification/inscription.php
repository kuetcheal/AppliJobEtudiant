    <script>
    $(document).ready(function() {
        $('#registrationForm').submit(function(e) {
            e.preventDefault();

            $.ajax({
                url: 'Authentification/inscription-ajax.php',
                type: 'POST',
                data: $(this).serialize(),
                dataType: 'json',
                success: function(response) {
                    if (response.status === "success") {
                        $('#registrationMessage').html(
                            '<div class="alert alert-success">' + response.message +
                            '</div>');
                        $('#registrationForm')[0].reset();
                    } else {
                        $('#registrationMessage').html(
                            '<div class="alert alert-danger">' + response.message +
                            '</div>');
                    }
                },
                error: function() {
                    $('#registrationMessage').html(
                        '<div class="alert alert-danger">Une erreur est survenue.</div>'
                    );
                }
            });
        });
    });
    </script>

